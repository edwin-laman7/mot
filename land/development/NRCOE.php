				<h2>The Role of National Rail Center of Excellence</h2>
				<p>The Role of NCROE is to provide a <u>centralized platform</u> to:</p>
				<ol>
					<li>Implement the Human Capital Development (HCD), Rail Industry Development Program (RIDP), Testing and Certification (T&C), and Research and Technology (R&T) by remaining susataiable in general.</li>
					<li>Become the facilitator for Rail Industry Development in Malaysia.</li>
					<li>Become the Hub and the front-line interface for international and local industry players to interact and to provide professional advise on Rail Industry, leveraging on ICP.</li>
				</ol>
				<p>NRCOE shall operate as an entity under auspices of Ministry of Transport to facilitate and implementing a strict governance between the government, agencies, academia and industry player.</p>
				<br>
