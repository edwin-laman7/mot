				<h3>ASEAN Road Safety Centre​</h3>
				<p>Proposal for the ASEAN Road Safety Centre (ARSC) establishment was presented on May 14, 2013 at the 4th Meeting of the Road Safety Group Special ASEAN Multi Sector Work (MRSSWG) by MIROS. The proposed center will provide knowledge about the state of road safety in ASEAN member countries including information on laws and regulations, data on traffic accidents, road traffic data, rules on standards and vehicles inspection, driver training and driver testing, activities of the traffic police, road safety training in schools, road safety information / campaigns to the public.​</p>

				<p>​After the 20th ASEAN Transport Ministers Meeting (ATM) on 27 November 2014 in Mandalay, Myanmar welcomed the inauguration of the Institute of Road Safety Research (Miros) as the ASEAN Regional Road Safety Center. The goal of the center is to promote and provide knowledge on issues of road safety among ASEAN member countries.​</p>

				<p>​​ASEAN Road Safety Centre will also serve as a center that will help and guide the ASEAN countries to achieve higher levels of road safety performance in the following aspects:</p>

				<ol>
					<li>The program of road safety and implement science-based design and art in the region;​</li>
					<li>Harmonising standards, guidelines and rules of road safety activities in the region;​</li>
					<li>Coordinate and gather the efforts of member countries for the purpose of sharing knowledge;​</li>
					<li>Provide technical guidance and effective leadership to Member States;​</li>
					<li>Monitor and evaluate the implementation of road safety programs and initiatives by member states; and</li>
					<li>Evaluate and verify road safety performance of the member states, to combine, coordinate and compare aspects of road safety in the region that would act as the overall region’s representative.​</li>
				</ol>

				<h3>ASEAN NCAP</h3>
				<p>The planned program is to develop new assessment program to kebolehlanggaran vehicles, based on the data belong to Malaysian manufacturers and translated and revalued based on certain criteria. In total there are 8 such programmes, including in the United States, Europe and Australasia region, also Asia.</p>

				<p>The main goal of this program is to provide security related information to the public through vehicle collision test full scale independent and objective. In comparison, NCAP tests are performed normally at a level exceeding the minimum requirements of the regulations and the law specified. The program also directly mempertabatkan vehicle manufacturer's effort to issue a safer vehicle to market.</p>

				<p>ASEAN is the result of cooperation between NCAP MIROS and globally with the aim of improving NCAP safety levels of vehicles in the Asean region. It is a project for dilahirkankan heed the United Nations rise to the Decade of Action 2011-2020 for Road Safety.</p>
