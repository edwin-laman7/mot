					<li>Anti-lock Braking System (ABS) is widely fitted in all variants of new passenger cars.</li>
					<li>Electronic Stability Control (ESC) under the regulation UN R140 is a mandatory fitment for new passenger cars in Malaysia since July 2018.</li>
					<li>ASEAN NCAP stresses the importance of Blind Spot Technology (BST) or Blind Spot Visualization (BSV) fitment in order to avoid collision with motorcyclist or minimize injuries if such incident occurred.</li>
					<li>Autonomous Emergency Braking (AEB), a technology fitment that is able to automatically apply braking for the car in an emergency situation when the driver is unaware of the car or pedestrian in front or made a wrong estimation on the distance between his/her car and the one in front of it. There are currently three types of AEB that are available in cars sold in the global market namely AEB City, AEB Inter-Urban and AEB Pedestrian/VRU.</li>
