			<p>The term ‘safer cars’ with relation to the new car assessment program (NCAP) is to distinguish one vehicle from another in terms of safety performances, primarily on two aspects namely, crashworthiness and active safety. Crashworthiness is how well the car protects its occupant in a crash whilst active safety refers how the car avoids/mitigates the crash from occurring. The speed of the crash test that is conducted under the NCAP program is also higher compared to regulation in which the speed requirement for frontal offset crash test under regulation is 56 km/h whilst NCAP is 64 km/h. Furthermore, each NCAP program around the world has developed its own protocol that takes into consideration the road safety scenario and condition of the region/countries where the NCAP is promoting safer car initiative. The protocol developed by each NCAP is targeted to enhance the car’s safety performance. By successfully achieving the scoring points determined by the NCAP program, vehicle manufacturers will be rewarded with the NCAP rating between 0-Star and 5-Star ratings. Thus, in Malaysia, there has been a growing number of safer cars in the market. As of March 2020, 81% of passenger car models sold in Malaysia have successfully obtained ASEAN NCAP rating of 4-Star and above.</p>
			
			<p>With the successful implementation of ASEAN NCAP, Malaysia under the Ministry of Domestic Trade and Consumer Affairs (KPDNHEP) will be implementing the ASEAN NCAP Labelling as a mandatory regulation by 2020. The mandatory label which contains information about ASEAN NCAP and also other NCAPs (if any) safety ratings as well as the safety specifications of the car model needs to be displayed on all passenger cars that are being showcased in vehicle showrooms or any promoting premises in Malaysia. Through the label consumers visiting the showrooms will be informed on the safety performance of the car and the safety features offered in the displayed model. As such, by having such a label it can give significant influence towards consumers’ purchasing decisions when they are planning to buy a new car.</p>

			<p>The features or components relating to safer cars:
				<ol>
