	<h2>RTS Project To Be Inked On July 30</h2>
			<p>
		PRIME Minister Tan Sri Muhyiddin Yassin and his Singaporean counterpart Lee Hsien Loong will witness the signing of agreements on July 30 for the
		development of the Rapid Transit System (RTS) project between Johor Baru and Singapore. Transport Minister Datuk Seri Dr Wee Ka Siong said this after informing lawmakers that both nations had succeeded in reaching an agreement following recent negotiations on the project. “I am happy to announce that negotiations for the bilateral agreement, the concession agreement and the co-op joint venture agreement have
		concluded. “The signing of the agreements will be held at a ceremony in Tambak, Johor, on July 30, which will be witnessed by the leaders of
		Malaysia and Singapore, ” Dr Wee said when replying to a question by Akmal Nasrullah Mohd Nasir (PH-Johor Baru).
		</p>
		<p>Dr Wee told lawmakers that construction of the RTS would be carried out immediately once the necessary approvals were in order. “Work on the RTS is scheduled to begin in January next year and is expected to be fully operational by the end of 2026, ” he said. The minister added that the project would be carried out in two phases involving the construction phases, between 2021 and 2024 and the system testing phase, between 2025 and 2026.</p>
		<p>
		Dr Wee said the project would help to create jobs for locals, as well as spur economic growth in the surrounding Bukit Chagar area. He said considerations had also been given to ensure the RTS would be affordable to commuters. The RTS project will cover 4km of rail linking Bukit Chagar in Johor Baru and Woodlands in Singapore, with the capacity to ferry 10,000 passengers per hour.</p>
		<p>
		Later at a press conference, Dr Wee said the cost of the project was estimated at RM3.7bil from RM3.16bil projected earlier, after taking into account the cost for the relocation of the depot from Singapore to Johor Baru. This, he said, would include the cost of building the tracks from Bukit Chagar to Wadihana that required security features needed by both countries.</p>

