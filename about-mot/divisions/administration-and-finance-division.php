<h1>Administration and Finance Division</h1>
<h2>Background</h2>
<p>​The Administration and Finance Division was formally established on July 15, 2010. The establishment is in line with the restructuring program of the Ministry of Transport which combines the existing Finance Division with Administration Unit and Asset Unit which were previously under the management of the Human Resources Management Division. </p>

<p>This division is responsible for budget management, procurement, asset, finance and administration of the Ministry of Transport and Departments / Agencies.</p>

<h2>Client's Charter </h2>
<ul>
<li>Prepare and review the annual budget estimate of the Ministry and the Department of Budget Management and Development within 2 months from the date of the letter issued by the Treasury.</li>
<li>Allocating the warrants according to the Ministry and the Department of Budget Management and Development’s annual budget estimates within 5 working days after receiving General Warrant from the Treasury.</li>
<li>Review and process applications transfer / the complete additional allocation from the Ministries and Departments within 7 working days.</li>
<li>Hold monthly meeting for the Board of Tender / Quotations Committee and deliver results within one day.</li>
<li>Manage the tender process within 60 days after receiving the specifications and documents from Division / Unit.</li>
<li>Manage the process for Quotation within 30 days after receiving the specifications and documents from Division / Unit.</li>
<li>Issue Letter of Acceptance within 1 working day after receiving the Treasury’s final decision or the Board of Tender / Quotation Committee.</li>
<li>Issuing the Local Order / Government through eP system and eSPKB within 3 days after the completed application is received and approved.</li>
<li>Review and process the payment of bills / complete documents of operation and development expenditures within a period of 9 days.</li>
<li>Review and process applications of Self Payment, Warm Clothing Allowance, Computer Financing and Car Loan within 7 working days from the date the completed application is received.</li>
<li>Ensure that issues and complaints are answered within 3 working days.</li>
<li>Ensure that applications for going overseas (on personal business) to be processed within 2 days from the date after the completed application is received.</li>
<li>Manage the maintenance of office equipment, furniture and vehicles according to the General Administration’s schedule to ensure readiness at all times.</li>
<li>Manage meeting and event by the Management Services in accordance with regulations in force.</li>
<li>Implement security patrols in the building twice (2) a day;Produce Security Pass within three (3) days of applications receipt ; andImplement and settling in full the service and loan / return of library materials immediately​</li>
</ul>

<h2>Functions</h2> 
<ul>
<li>Managing of the preparation and examining the operation and development budgets for the Ministries, Departments and Agencies under the Ministry of Transport.</li>
<li>Managing the development expenditure in the Ministry and controls administration and development expenditure for Ministries, Departments and Agencies under the Ministry.</li>
<li>Managing matters pertaining to untraceable items, disposal and write-off of government property for Ministries and Departments under the Ministry.</li>
<li>Managing tenders and contracts, appointment of consultants and contractors, procurement of work, services and supply of goods under the jurisdiction of the Ministries, Departments and Agencies under the Ministry.</li>
<li>Coordinating the security of document, physical, personal and record for the Ministries and Agencies and Departments.</li>
<li>Secretariat for the following committees such as Financial and Account Management Committee, the Board of Procurement, Price Negotiations Committee, Tender Committee, Task Force on eProcurement, Management Meeting, Morning Meeting and Celebration / Events in the Ministry.</li>
<li>Coordinate the following appointments : The Board of Directors of the Statutory Bodies under the Ministry of Transport, -Consultant Committee of the Statutory Bodies under the Ministry of Transport,-Pilot Committee of the Statutory Bodies under the Ministry of Transport,-Board Members from amongst the ministry officials of the Ministry and Statutory Bodies -​​Board Members for the Directors of GLCs under the Ministry of Transport.</li>
</ul>

<h2>​​​Objectives</h2> 

<ul>
<li>To provide financial support, budgets and contracts to departments and agencies under the Ministry of Transport.</li>
<li>To ensure the monitoring of all policies and financial affairs, budget and government contracts are carried out efficiently and effectively.</li>
<li>To provide efficient and effective administrative support services to the Division / Unit within the Ministry in matters relating to the Secretariat and Committee Meetings of the Ministry, Administration and Office Support Services, Safety and Records. </li>
</ul>

