<h1>Bahagian Pembangunan</h1>
<h2>Latar Belak​​ang​ </h2>

Bahagia​n Pembangunan telah diwujudkan pada 21 Oktober 2009 melalui waran perjawatan Bi​l.K116 Tahun 2008 bertarikh 13 Oktober 2008. Bahagian ini pada asalnya adalah Unit Pemantauan Projek dan dikembangkan menjadi Cawangan Pembangunan pada Oktober 2007. Menyedari akan keperluan yang semakin mendesak​ terutamanya isu-isu pembangunan , pada Disember 2007 Tan Sri KSN telah mengarahkan semua Kementerian mewujudkan Bahagian Pembangunan untuk memantau dan meyelaras projek-projek pembangunan Kementerian agar berjalan dengan lancar serta mengikut jadual dan kos yang telah ditetapkan. Bahagian ini terdiri daripada lima (5) unit utama iaitu Unit Pemantaun dan Penilaian Projek, Unit Pelaksanaan Projek Darat, Unit Pelaksanaan Projek Udara dan Maritim dan Unit Pengurusan Tanah dan Teknikal serta Unit Pasukan Khas Gemas – Johor Bharu. Jumlah keseluruhan kakitangan Bahagian ini adalah seramai 32 orang.​

<h2>Piagam Pelanggan ​​</h2>

<ul>
<li>Memastikan projek-projek pembangunan RMK mengikut sektor iaitu Darat (kecuali jalan dan jambatan), Udara dan Maritim yang dirancang dimulakan dalam tempoh 21 bulan dalam tahun pertama;</li>
<li>Memastikan isu dan aduan mengenai projek-projek pembangunan dijawab dalam tempoh 3 hari bekerja;</li>
<li>Memastikan maklumat dalam Sistem Pemantauan Projek (SPP) sentiasa dikemaskini setiap 2 minggu;</li>
<li>Menyelaras dan mengemukakan permohonan tanah dari agensi/jabatan kepada pihak JKPTG dalam tempoh 7 hari bekerja dan memastikan maklumat sentiasa dikemaskini;</li>
<li>Menyelaras penilaian ‘outcome’ dan impak projek pembangunan yang telah siap dilaksanakan pada setiap bulan;</li>
<li>Mengatur latihan-latihan berhubung dengan pelaksanaan projek pembangunan 2 kali setahun;</li>
<li>Mengadakan sesi pengkongsian ilmu mengenai pembangunan setiap bulan.Memeriksa dan mengawal projek/ komponen projek secara menyeluruh agar setiap pelaburan yang dibuat oleh Kerajaan mendapat pulangan yang setimpal “value for money” dan projek berfungsi seperti yang diharap.</li>
</ul>

<h2>Fun​​gsi </h2>

​<ul>
<li>Mengurus perancangan dan pembangunan Rancangan Malaysia Lima Tahun bagi sektor darat, udara dan maritim;</li>
<li>Memantau sistem maklumat projek-projek pembangunan Kementerian;</li>
<li>Menilai keberkesanan penyelarasan dan pengawasan dalam pelaksanaan projek;</li>
<li>Menyemak anggaran belanjawan pembangunan RMLT (Mid Term Review)​​Menyelaras dan membantu Jabatan/Agensi berkaitan permohonan dan isu - isu tanah.</li>
<li>Membantu pegawai pembangunan di Kementerian dan Jabatan memiliki kompentensi tertentu bagi melaksanakan tugas-tugas pembangunan dengan mengadakan bengkel projek dan tanah setiap tahun.​​​</li></ul>

<h2>Objekt​​if</h2>

<ul><li>Menentukan program, projek dan aktiviti pembangunan Kementerian dilaksanakan secara bersepadu dan berkesan untuk membangunkan dan menyediakan infrastruktur sistem pengangkutan awam yang moden dan praktikal.</li>
<li>Memastikan perbelanjaanperuntukan pembangunan Kementerian dibuat secara optimum berdasarkan perancangan yang ditetapkan.</li>
<li>Mengurus, mengawal serta memantau pelaksanaan projek-projek pembangunan Jabatan/Agensi di bawah Kementerian mengikut tempoh masa yang ditetapkan.</li></ul>

