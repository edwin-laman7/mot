​​​​​​​​​​​​​​​​​​​​​​​<h1>Bahagian Pentadbiran dan Kewangan</h1>

<h2>Latar Belak​​ang ​</h2>
<p>Bahagian Pentadbiran dan Kewangan secara rasminya telah ditubuhkan pada 15 Julai 2010. Penubuhan ini selaras dengan program penstrukturan semula Kementerian Pengangkutan yang menggabungkan Bahagian Kewangan sedia ada dengan Unit Pentadbiran dan Unit Aset yang sebelum ini di bawah pengurusan Bahagian Pengurusan Sumber Manusia. Bahagian ini bertanggungjawab dalam pengurusan belanjawan, perolehan, aset, kewangan dan pentadbiran Kementerian Pengangkutan dan Jabatan / Agensi di bawahnya. </p>

<h2>Piagam Pelanggan </h2>
<ul>
<li>Menyedia dan memeriksa anggaran belanjawan tahunan Kementerian dan Jabatan bagi Anggaran Belanja Mengurus dan Pembangunan dalam tempoh 2 bulan daripada tarikh surat Perbendaharaan.</li>
<li>Membahagikan waran anggaran belanjawan tahunan Kementerian dan Jabatan bagi Anggaran Belanja Mengurus dan Pembangunan dalam tempoh 5 hari bekerja selepas menerima Waran Am daripada Perbendaharaan.</li>
<li>Menyemak dan memproses permohonan pindah / tambah peruntukan yang lengkap daripada Kementerian dan Jabatan dalam tempoh 7 hari bekerja.</li>
<li>Mengadakan Mesyuarat LembagaTender/ Jawatankuasa Sebut harga setiap bulan dan menyampaikankeputusandalam masa 1 hari.</li>
<li>Mengurus proses Tender dalam tempoh 60 hari selepas menerima spesifikasi dan dokumen yang lengkap daripada Bahagian/Unit.</li>
<li>Mengurus proses Sebutharga dalam tempoh 30 hari selepas menerima spesifikasi dan dokumen yang lengkap daripada Bahagian/Unit.</li>
<li>Mengeluarkan Surat Setuju Terima dalam tempoh 1 hari bekerja selepas menerima keputusan muktamad Perbendaharaan atau Lembaga Tender/Jawatankuasa Sebut Harga.</li>
<li>Mengeluarkan Pesanan Tempatan/Kerajaan melalui sistem eP dan eSPKB dalam tempoh 3 hari selepas permohonan yang lengkap dan diluluskan diterima.</li>
<li>Menyemak dan memproses bayaran perbelanjaan mengurus dan pembangunan bagi bil / dokumen yang lengkap dalam tempoh 9 hari.</li>
<li>Menyemak dan memproses permohonan Pendahuluan Diri, Elaun Pakaian Panas, Pembiayaan Komputer dan Pinjaman Kenderaan dalam tempoh 7 hari bekerja daripada tarikh permohonan yang lengkap diterima.</li>
<li>Memastikan isu dan aduan dijawab dalam tempoh 3 hari bekerja.</li>
<li>Memastikan permohonan untuk keluar negara (atas urusan persendirian) diberi jawapan dalam tempoh 2 hari dari tarikh permohonan yang lengkap diterima.</li>
<li>Pengurus penyelenggaraan peralatan pejabat, perabot dan kenderaan Pentadbiran Am mengikut jadual supaya sentiasa berada dalam keadaan sedia untuk digunakan pada setiap masa.</li>
<li>Pengurusan mesyuarat dan majlis oleh Bahagian Khidmat Pengurusan dilaksanakan mengikut peraturan berkuat kuasa.</li>
<li>Melaksanakan rondaan keselamatan di dalam bangunan dua (2) kali sehari;Pengeluaran Pas K​eselamatan dalam tempoh tiga (3) hari dari tarikh permohonan lengkap diterima; danMelaksanakan dan menyelesaikan perkhidmatan pinjaman/pemulangan bahan bacaan perpustakaan serta merta.</li>
</ul>

<h2>​Fun​gsi</h2>

<ul>
<li>Pengurusan penyediaan dan pemeriksaan belanjawan mengurus dan pembangunan untuk Kementerian, Jabatan-jabatan dan Agensi-agensi di bawah Kementerian Pengangkutan.</li>
<li>​​​Pengurusan pembayaran perbelanjaan pembangunan di peringkat Kementerian dan pengawalan perbelanjaan mengurus dan pembangunan untuk Kementerian, Jabatan-jabatan dan Agensi-agensi di bawah Kementerian.</li>
<li>Pengurusan hal-hal kehilangan, pelupusan dan hapuskira harta benda kerajaan untuk Kementerian dan Jabatan-jabatan di bawah Kementerian.</li>
<li>Pengurusan hal-hal tender dan kontrak, perlantikan juruperunding dan kontraktor, perolehan kerja, perkhidmatan dan pembekalan barang-barang di bawah bidang kuasa Kementerian, Jabatan-jabatan dan Agensi-agensi di bawah Kementerian.</li>
<li>Menyelaras urusan keselamatan dokumen, fizikal, peribadi dan rekod Kementerian dan Agensi serta Jabatan di bawahnya.</li>
<li>Menjadi urus setia kepada jawatankuasa-jawatankuasa yang berikut Jawatankuasa Pengurusan Kewangan dan Akaun, Lembaga Perolehan, Jawatankuasa Rundingan Harga, Jawatankuasa Tender, Jawatankuasa Pasukan Petugas ePerolehan, Mesyuarat Pengurusan, Mesyuarat Pagi dan Majlis Sambutan / Acara Kementerian.</li>
<li>Menyelaras urusan perlantikan berikut J​emaah Lembaga Pengarah Badan-badan Berkanun di bawah Kementerian Pengangkutan, -Jawatankuasa Perunding Badan-badan Berkanun di bawah Kementerian Pengangkutan,-Jawatankuasa Pemaliman Badan-badan Berkanun di bawah Kementerian Pengangkutan,-Ahli Lembaga Pengarah dari kalangan P​egawai Kementerian bagi Badan Berkanun di Kementerian dan -​​Ahli Lembaga Pengarah bagi Syarikat Berkaitan Kerajaan di bawah Kementerian Pengangkutan.</li>
</ul>
