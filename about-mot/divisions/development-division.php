<h1>Development Division</h1>
<h2>Background</h2>

<p>On 21st October 2009, the Development Division of Ministry of Transport was established under the Establishment Warrant Bil.K116 Year 2008 dated 13th October 2008. It started as a small unit called Project Monitoring Unit before establishing itself as a Development Branch. In December of the same year, Tan Sri KSN instructed every ministry to establish its own development division. This was to ensure projects were done smoothly and efficiently. There are five (5) units in the division i.e. Project Monitoring and Assesment Unit, Land Management and Technical Unit, Aviation and Maritime’s Project Implementation Unit, Land Unit and Dedicated Team of Gemas – JB (Double Track Electrification). As of 15 of August 2011, there are 32 posts in the division. </p>

<h2>Client's Charter </h2>

<ul>
<li>To ensure projects managed by Develepmont Division under RMK starts within 21 months of its first year;</li>
<li>To respond to customers’ complaints and/or feedbacks within 3 working days;</li>
<li>To ensure all informations in the Projects Monitorong System (SPP) are updated fortnightly;</li>
<li>To manage and liaise all land issues/applications forwarded by agencies under MOT to JKPTG within 7 working days;</li>
<li>To manage the outcome and impact evaluation of all completed projects every month;</li>
<li>To arrange Projects Management related training 2 times a year;</li>
<li>To arrange seminar/talks every month;</li>
<li>To monitor and evaluate projects for its efficacy and ‘value for money. </li>
</ul>

<h2>Functions</h2> 

<ul>
<li>Manage the planning and development of five-year Plans for land, air and maritime;</li>
<li>Monitoring information systems development projects of the Ministry;</li>
<li>Assess the effectiveness of coordination and supervision of the project implementation;</li>
<li>Check the development budgets RMLT (Mid Term Review);</li>
<li>Coordinate and assist departments/agencies in relation to the application and land issues;</li>
<li>Help development officers in ministries and departments had the competency to perform development tasks with project workshop and land every year. </li>
</ul>

<h2>Objectives</h2> 

<ul>
<li>To assist in evaluating and determine the effectiveness of all the internal control process and governance of the ministry and all the Department and Agencies under MOT throughout systematic and disciplinary approach.​​</li>
<li>To ensure all allocations given to Development Division were properly managed and optimised;</li>
<li>To organize, manage and monitor the timeline development of projects under the ministry.​</li>
</ul>

