			<h2>Profil Malaysia Sebagai Ahli Majlis ICAO</h2>
			<p>​ICAO berperanan menetapkan piawaian dan peraturan penerbangan awam antarabangsa serta berfungsi sebagai forum kerjasama di antara 192 Negara Pejanjinya. Malaysia telah menjadi Negara Pejanji ICAO sejak 7 Mei 1958 dan sentiasa mengambil bahagian dalam program dan aktiviti yang dianjurkan oleh Pertubuhan tersebut.​</p>

			<h3>Kean​ggotaan​</h3>
			<p>Majlis ICAO pula merupakan badan eksekutif ICAO yang dianggotai oleh 36 buah Negara Pejanji yang dipilih melalui undian. Disebabkan oleh penglibatan aktif tersebut, Malaysia telah berjaya menjadi Ahli Majlis ICAO sejak tahun 2007 dan mengekalkan keahlian dalam Majlis selama tiga penggal berturut-turut.</p>

			<h3>Pelantikan Mal​aysia sebagai ahli Majlis​​​​</h3>
			<p>Pelantikan Malaysia sebagai ahli Majlis membolehkan Negara menyumbang kepada penerbangan awam antarabangsa dan memberi akses untuk menyuarakan secara terus perkara yang melibatkan kepentingan nasional. Ia juga meningkatkan imej dan profil Negara di peringkat antarabangsa.</p>

			<h2>Kerjasama Malaysia Dengan ACAC, AFCAC, ECAC Dan LACAC​</h2>
			<p>Bagi menjamin pemilihan semula Malaysia, Kementerian Pengangkutan dan Jabatan Penerbangan Awam telah mengatur strategi untuk mendapatkan sokongan daripada negara-negara Kumpulan Serantau. Kumpulan Serantau tersebut yang mewakili kedudukan geografi dunia terdiri daripada Arab Civil Aviation Commission (ACAC), African Civil Aviation Commission (AFCAC), European Civil Aviation Conference (ECAC) dan Latin American Civil Aviation Commission (LACAC).</p>

			<p>Untuk mendapatkan sokongan Kumpulan Serantau bagi pemilihan Ahli Majlis ICAO, Malaysia telah menjalinkan hubungan kerjasama di dalam bidang penerbangan awam dengan Kumpulan Serantau tersebut. Sebelum ini, Malaysia telah menandatangani Memorandum Persefahaman (MoU) dengan LACAC pada tahun 2011 dan AFCAC pada tahun 2014. Strategi ini ternyata berkesan apabila Malaysia telah dipilih semula sebagai ahli Majlis ICAO buat kali ketiga berturut-turut.​</p>

			<h3>MOU</h3>
			<p>Pada tahun 2015, Malaysia telah memperbaharui MoU dengan LACAC dan merancang untuk menandatangani MoU dengan ECAC dan ACAC. MoU ini merupakan salah satu strategi untuk mengekalkan Malaysia sebagai ahli Majlis ICAO bagi penggal 2016 - 2019.​</p>

			<h2>Fellowship Programme Antara Malaysia Dan ICAO​</h2>
			<p>Program Kerjasama Teknikal Malaysia (PKTM) merupakan satu komitmen Malaysia dalam mempromosikan kerjasama teknikal di antara Negara-negara membangun dan mengukuhkan kerjasama serantau. Program ini juga merupakan salah satu strategi Malaysia untuk mendapatkan sokongan Kumpulan Serantau. Akademi Penerbangan Malaysia (MAvA) di bawah Jabatan Penerbangan Awam telah dipilih sebagai sebuah institusi latihan bagi menjalankan program latihan dalam bidang penerbangan awam.​</p>
			<p>Sehingga kini, seramai 368 orang pegawai penerbangan awam dari 63 Negara telah dilatih dan mendapat manfaat daripada program ini yang ditaja sepenuhnya oleh Kerajaan Malaysia.​</p>

