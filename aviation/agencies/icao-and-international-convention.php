			<h2>Profile of Malaysia as a Council Member of the ICAO</h2>
			<p>Established in 1944 and following the signing of the Convention on International Civil Aviation (Chicago Convention) by the Contracting States, The International Civil Aviation Organization (ICAO) is a United Nation specialized agency.</p>
			<p>ICAO's role is to set standards and rules regarding international civil aviation as well as serving as a forum for cooperation among the 192 contracting countries. Malaysia has been a member of ICAO since May 7, 1958 and has been continuously taking part in programs and activities organized by the organization.</p>

			<h3>Membership</h3>
			<p>The ICAO Council, which comprises of 36 states selected via election from members of the assembly is the governing body of ICAO. Malaysia has been a Council Member of ICAO from 2007 and has since maintains membership in the Council for three consecutive terms</p>

			<h3>The appointment of Malaysia as a council member​​</h3>
			<p>The appointment of Malaysia as a council member of the ICAO enables Malaysia to contribute to the international civil aviation industry and provides direct access to voice matters affecting the national interest. Simultaneously, the membership enhances the image and profile of Malaysia at the international level.</p>

			<h2>Cooperation Between Malaysia with ACAC, AFCAC, ECAC AND LACAC​</h2>
			<p>To ensure that Malaysia is re-elected as a council member, the Ministry of Transport and the Department of Civil Aviation Malaysia has set up several strategies to gain support from countries in the Regional Group. The regional groups representing the geographical representation comprises of the Arab Civil Aviation Commission (ACAC), African Civil Aviation Commission (AFCAC), European Civil Aviation Conference (ECAC) and the Latin American Civil Aviation Commission (LACAC).</p>

			<p>Among the strategies that Malaysia has established with the Regional Group of ICAO Council is signing of Memorandum of Understanding (MoU) in the field of civil aviation exemplified by the signing MoU with LACAC in 2011 and subsequently with AFCAC in 2014. This strategy was effective in ensuring Malaysia’s re-election into the ICAO Council for the third time consecutively.</p>

			<h3>MOU</h3>
			<p>In 2015, Malaysia renewed its MoU with LACAC and is in the process of signing a MoU with the ECAC and ACAC. This MoU is one of the efforts undertaken by Malaysia to remain as an ICAO member for the 2016-2019 session.​</p>

			<h2>Fellowship Programme Between Malaysia and ICAO</h2>
			<p>The Malaysian Technical Cooperation Programme (MTCP) is an example of Malaysia's commitment in promoting technical cooperation among developing countries and strengthening regional cooperation. The Malaysia Aviation Academy (MAvA) under the Department of Civil Aviation Malaysia for instansce has been nominated the aviation training centre for conducting aviation programmes for personnel around the world, and has established a reputation within the global aviation personnel training community.​</p>
			<p>To date, a total of 368 civil aviation officials from 63 countries have been trained and benefited from this program, which is sponsored by the Malaysian Government.</p>
